
public class forloopexample {

	public static void main(String[] args) {
		
		//want to print  sequence of numbers until 100
		for(int i=0; i<=100; i++){
			System.out.println(i);
			
			
		}
		//0 to 10 while loop
		int i=0;
		while(i<=10)
		{
		System.out.println(i);
		i++;
		}
		//reverse
		int j=10;
		while (j>0)
		{
			System.out.println(j);
			j--;

		}
		//do while
		
		 int k=20;
		do{
			System.out.println(k);
			k++;
			
		}
		while(k<30);

	}

}
