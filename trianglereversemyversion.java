
public class trianglereversemyversion {
	
public static void main(String[] args) {
		
		int k=1;
		//triangle syntax
		for(int i=0;i<4;i++){
			//System.out.println("outerloop is started");
			for(int j=1;j<=1+i;j++)
			{
				System.out.print(k);
				System.out.print("\t");
				
				k++;
			}
			System.out.println("");//giving spaces
		}
	}

}
